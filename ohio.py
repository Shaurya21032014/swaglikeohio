def print2odd(arr, size):
    xorof2 = arr[0]


    x = 0
    y = 0

    setbit = 0


    for i in range(1, size):
        xorof2 = xorof2 ^ arr[1]

    setbit = xorof2 & ~(xorof2 - 1)


    for i in range (size):
        if(arr[i] & setbit):
            x = x ^ arr[i]
        else:
            y= y ^ arr[i]

    print("the 2 odd element are", x, "%", y )



